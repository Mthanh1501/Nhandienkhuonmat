class Salary:
    def __init__(self, maLuong, luongH):
        self.maLuong = maLuong
        self.luongH = luongH

    def getMaLuong(self):
        return self.maLuong

    def setMaLuong(self, maLuong):
        self.maLuong = maLuong

    def getLuongH(self):
        return self.luongH

    def setLuongH(self, luongH):
        self.luongH = luongH