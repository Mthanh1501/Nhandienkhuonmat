class Position:
    def __init__(self, maCV, tenCV):
        self.maCV = maCV
        self.tenCV = tenCV

    def getMaCV(self):
        return self.maCV

    def setMaCV(self, maCV):
        self.maCV = maCV

    def getTenCV(self):
        return self.tenCV

    def setTenCV(self, tenCV):
        self.tenCV = tenCV